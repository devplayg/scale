package scale

import "math/rand"

func randomByte() int {
	//return randomInt(65, 90)
	return randomInt(97, 112)
}

func randomString(len int) string {
	bytes := make([]byte, len)
	for i := 0; i < len; i++ {
		bytes[i] = byte(randomInt(65, 90))
	}
	return string(bytes)
}

func randomInt(min, max int) int {
	return min + rand.Intn(max-min)
}
